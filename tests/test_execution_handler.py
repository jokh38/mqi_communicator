def test_handler_initialization():
    """Test handler initialization requires settings."""
    with pytest.raises(TypeError):
        ExecutionHandler(mode="local") # Missing settings

    mock_settings = MagicMock(spec=Settings)
    handler = ExecutionHandler(settings=mock_settings, mode="local")
    assert handler.mode == "local"
    assert handler.settings is mock_settings

@patch("subprocess.run")
def test_execute_command_local_mode(mock_run):
    """Test raw command execution in 'local' mode."""
    handler = ExecutionHandler(settings=MagicMock(spec=Settings), mode="local")
    handler.execute_command("ls -l")
    mock_run.assert_called_once_with("ls -l", shell=True, check=True, capture_output=True, text=True, cwd=None)

def test_execute_command_remote_mode(mock_ssh_client):
    """Test raw command execution in 'remote' mode."""
    handler = ExecutionHandler(settings=MagicMock(spec=Settings), mode="remote", ssh_client=mock_ssh_client)
    handler.execute_command("ls -l", cwd="/tmp")
    mock_ssh_client.exec_command.assert_called_once_with("cd /tmp && ls -l")

