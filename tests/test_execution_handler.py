@patch.object(ExecutionHandler, 'execute_command')
def test_submit_simulation_job_uses_settings_for_remote(mock_execute_command, settings, mock_ssh_client):
    """
    Tests that submit_simulation_job gets a 'remote' command from Settings and executes it.
    """
    # Arrange
    settings.execution_handler["HpcJobSubmitter"] = "remote"
    mock_execute_command.return_value = ExecutionResult(success=True, output="Submitted batch job 12345", error="", return_code=0)
    handler = ExecutionHandler(settings=settings, mode="remote", ssh_client=mock_ssh_client)

    # Expected command is generated by the Settings object using the correct key
    expected_command = settings.get_command("remote_submit_simulation", handler_name="HpcJobSubmitter", case_id="C-REMOTE")
    assert "sbatch" in expected_command # Sanity check

    # Act: Pass the correct command_key
    result = handler.submit_simulation_job(
        handler_name="HpcJobSubmitter",
        command_key="remote_submit_simulation",
        case_id="C-REMOTE"
    )

    # Assert
    mock_execute_command.assert_called_once_with(expected_command)
    assert result.success
    assert result.job_id == "12345"

@patch.object(ExecutionHandler, 'execute_command')
def test_post_process_uses_settings_for_local(mock_execute_command, settings):
    """
    Tests that post_process gets a 'local' command from Settings and executes it.
    """
    # Arrange
    settings.execution_handler["PostProcessor"] = "local"
    handler = ExecutionHandler(settings=settings, mode="local")

    context = {"case_id": "C-LOCAL", "input_file": "/test/input.raw", "output_dir": "/test/output"}
    expected_command = settings.get_command("post_process", handler_name="PostProcessor", **context)
    assert "/usr/bin/python3" in expected_command # Sanity check

    # Act
    handler.post_process(handler_name="PostProcessor", **context)

    # Assert
    mock_execute_command.assert_called_once_with(expected_command, cwd=None)


